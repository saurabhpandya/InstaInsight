    private void getMediaLikes() {

        if (mInstagramSession.isActive()) {

            InstagramUser instagramUser = mInstagramSession.getUser();

            if (isConnected()) {

//                instaInsightApp.getUserBeanObserver().setUpdate(false);
                InstagramRequest request = new InstagramRequest(mInstagramSession.getAccessToken());
                request.createRequest("GET", Constants.WebFields.ENDPOINT_RECENT_MEDIA, new ArrayList<NameValuePair>(),
                        new InstagramRequest.InstagramRequestListener() {
                            @Override
                            public void onSuccess(String response) {

                                MediaDao mediaDao = new MediaDao(getApplicationContext());
                                ArrayList<MediaBean> aryLstMediaBean = mediaDao.getMediaDetailsFromJson(response);
                                aryLstMediaBean = mediaDao.saveMedia(aryLstMediaBean);
                                Log.d(TAG, "getMediaLikes:aryLstMediaBean::" + aryLstMediaBean.size());
                                init(aryLstMediaBean);
                            }

                            @Override
                            public void onError(String error) {
                                Utility.showToast(LikeGraphActivity.this, error);
                            }
                        });
            } else {
                MediaDao mediaDao = new MediaDao(getApplicationContext());
                ArrayList<MediaBean> aryLstMediaBean = mediaDao.getMedia();
                Log.d(TAG, "getMediaLikes:db::aryLstMediaBean::" + aryLstMediaBean.size());
            }
        } else {
            Utility.showToast(LikeGraphActivity.this, "Could not authentication, need to log in again");
            Intent intent = new Intent(LikeGraphActivity.this, LoginActivity.class);
            intent.addFlags(Intent.FLAG_ACTIVITY_CLEAR_TASK | Intent.FLAG_ACTIVITY_CLEAR_TOP);
            startActivity(intent);
            finish();
        }

    }
